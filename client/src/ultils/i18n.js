export const translate = (lang, key) => {
  const keys = key.split('.');
  let result = translations[lang];
  
  for (const k of keys) {
    if (result && typeof result === 'object') {
      result = result[k];
    } else {
      return key; // Return the key if translation not found
    }
  }
  
  return result || key;
};